		opt	6809
		opt	cd
		org	$4000
start		orcc	#$50

;**************************************
; double note values to save shifts
; on the sequencer (freqtab is words)
;**************************************
		ldx	#zix
convert		ldd	,x
		asla
		aslb	
		std	,x++
		cmpx	#endzix
		blo	convert
****************************************
* SOUND ON DAC
****************************************
		lda	$ff01			; Select DAC as sound source
		anda	#$f7
		sta	$ff01
		lda	$ff03
		anda	#$f7
		sta	$ff03
		lda	$ff23
		ora	#8
		sta	$ff23
;**************************************
; DP for note routine speed
;**************************************
;		lda	#note/256
;		tfr	a,dp
;**************************************
; setup vectored IRQ using HSYNC
;**************************************
		lda	#$7e			;DP JMP INSTRUCTION
		ldx	#note			;NOTE PLAY ADDRESS
		sta	$10c
		stx	$10d
;**************************************
; NUKE VSYNC IRQ
;**************************************
		lda	$ff03
		anda	#$fe
		sta	$ff03
		lda	$ff02
;**************************************
; ENABLE HSYNC IRQ
;**************************************
		lda	$ff01
		ora	#$1
		sta	$ff01
		lda	$ff00	
;**************************************
; CLEAR IRQ MASK ON THE CPU
;**************************************
		andcc	#$ef
;**************************************
; HANG ABOUT AND DO NUTHIN
; YOUR CODE GOES HERE SPUDZ !!
;**************************************
loop		inc	$400
		jmp	loop
;**************************************
; 2 VOICE NOTE ROUTINE
;**************************************
		opt	cc
		opt	ct
note		
sum		ldd 	#$0000		;CH1 OSCILLATOR
freq		addd 	#$0000 
		std	sum+1
sum2		ldd 	#$0000		;CH2 OSCILLATOR
freq2		addd 	#$0000 
		std 	sum2+1		;we have c2 in A - so add chan1
bb		adca	sum+1		;carry just incase
		rora			;ch1+ch2/2
		sta	$ff20		;OUT ON DAC
		dec	syncount	;DID WE DO THIS xx TIMES ?
		bne	out		;NO, OR ELSE SEQUENCE

;**************************************
; SEQUENCER
;**************************************
		opt	cc
		opt	ct
play2		com	wait		;TEMPO is 2x syncount (com is every other)
		bne	out
		lda	#$a0		;you can adjust syncount for tempo
		sta	syncount

oldu		ldu	#zix		;pattern position
		cmpu	#endzix
		blo	getnotes
		ldu	#zix		;we looped so start over
getnotes	pulu	d		;grab 2 notes and autoinc U
plnote		stu	oldu+1		;save U address for next seq
		sta	ft1+2		;fiddle LSB on freqtab1+2
		stb	ft2+2
		
ft1		ldx	#freqtab		
		ldx	,x		;YEAH YOU CAN !!!
		stx	freq+1		;store
ft2		ldx	#freqtab
ok1		ldx	,x		;get the right freq
stor1		stx	freq2+1	;store
out		lda	$ff00		;ack irq
		rti
;**************************************
; IRQ COUNTER
;**************************************
syncount	fcb	0
wait		fcb	0	


